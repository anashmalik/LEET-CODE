class Solution {
  public:
    int distributeTicket(int N, int K) {
        int i=1,p=0;
        while(i<N){
            if(p){ N-=K;
            p=0;
            
            
            
            }
            else {i+=K;
                p=1;
            }
        }
        if(p){
            return N;
        }
        return i;
    }
};


